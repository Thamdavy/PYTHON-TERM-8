# Inserting Elements into a List
# using the insert() method by specifying the index of the new element
motorcycles = ['honda', 'yamaha', 'suzuki']
motorcycles.insert(0, 'ducati')
print(motorcycles)

# Removing Elements from a List
motorcycles = ['honda', 'yamaha', 'suzuki']
motorcycles.pop()
print(motorcycles)

# Popping Items from any Position in a List by including the index
motorcycles = ['honda', 'yamaha', 'suzuki']
motorcycles.pop(1)
print(motorcycles)
